    private char[] EncryptString(String InputString,int KeySize)
    {
        char Input[]=new char[InputString.length()];
        char Output[]=new char[InputString.length()];
        Input=InputString.toCharArray();
            
        for(int z=0;z<InputString.length();z++)
        {
            if((Input[z]>='a'&&Input[z]<='z')||(Input[z]>='A'&&Input[z]<='Z'))
                Output[z]=Encrypt(Input[z],KeySize);
            else
                Output[z]=Input[z];
        }
        
        return Output;
    }
    
    private char Encrypt(char PlainChar,int KeySize)
    {
	int i = 0;
	char Temp = PlainChar;
	for (i = 0; i < KeySize; i++)
	{
	    if (Temp == 'z' || Temp == 'Z')
	    {
		switch (Temp)
		{
		    case 'z':
			Temp = 'a';
			break;
		    case 'Z':
			Temp = 'A';
			break;
		}
	    }
	    else
		Temp++;
	    
	}
	
	return Temp;
	
    }
    
